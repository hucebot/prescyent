"""Config elements for AndyData-lab-onePerson dataset usage"""
import os
from typing import List, Optional

from prescyent.dataset.config import DEFAULT_DATA_PATH, MotionDatasetConfig
from prescyent.dataset.features import Feature
from .metadata import FEATURES, SEGMENT_LABELS


class DatasetConfig(MotionDatasetConfig):
    """Pydantic Basemodel for AndyDataset configuration"""

    # url: str = "https://zenodo.org/records/3254403/files/xens_mnvx.zip?download=1"
    # """Url used to download the dataset"""
    data_path: str = os.path.join(
        DEFAULT_DATA_PATH, "AndyData-lab-onePerson", "xsens_mnvx"
    )
    """Directory where the data files is"""
    glob_dir: str = "Participant_*.mvnx"
    """Pattern used to find the list of files using a rglob method"""
    pt_glob_dir: str = "Participant_*.pt"
    """Pattern used to find the list of pt files, using a rglob method"""
    shuffle_data_files: bool = True
    """If True the list of files is shuffled"""
    use_pt: bool = True
    """If True, we'll search for .pt files (generated by this lib after a first loading of the dataset) instead of the full and slower to load .mnvx files"""
    used_joints: List[int] = list(range(len(SEGMENT_LABELS)))
    """Ids of the joints loaded. Default is all joints"""
    make_joints_position_relative_to: Optional[int] = None
    """None == Relative to world, else relative to joint with id == int"""
    ratio_train: float = 0.8
    """ratio of trajectories placed in Trajectories.train"""
    ratio_test: float = 0.15
    """ratio of trajectories placed in Trajectories.test"""
    ratio_val: float = 0.05
    """ratio of trajectories placed in Trajectories.val"""
    # Override default values with the dataset's
    history_size: int = 10
    """number of timesteps as input, default to 1s at 10Hz"""
    future_size: int = 10
    """number of predicted timesteps, default to 1s at 10Hz"""
    frequency: int = 10
    """The frequency in Hz of the dataset
    If different from original data we'll use linear upsampling or downsampling of the data
    Default is downsampling 240 Hz to 10Hz"""
    in_features: List[Feature] = FEATURES
    out_features: List[Feature] = FEATURES
    in_points: List[int] = list(range(len(used_joints)))
    out_points: List[int] = list(range(len(used_joints)))
